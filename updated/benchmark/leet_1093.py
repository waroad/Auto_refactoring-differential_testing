def sampleStats(count):
    (summ, minimum, maximum, mode, median, L, R, acc, n) = (0.0, -1, -1, 0, 0, -1, -1, 0, sum(count))
    for (i, item) in enumerate(count):
        summ += item * i
        if item > 0 and minimum == -1:
            minimum = i
        if item > 0:
            maximum = i
        if item > count[mode]:
            mode = i
        acc += item
        if acc == n // 2 and item > 0:
            L = i
        elif acc > n // 2:
            if R == -1:
                R = i
            if L == -1:
                L = R
    median = (L + R) / 2.0 if not n % 2 else R
    return [minimum, maximum, summ / n, median, mode]
