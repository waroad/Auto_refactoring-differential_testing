import importlib.util
ori_module_spec = importlib.util.spec_from_file_location('ori_module', '.\\examples\\assign4\\lazy.py')
test_module_spec = importlib.util.spec_from_file_location('test_module', '.\\updated\\assign4\\lazy.py')
ori_module = importlib.util.module_from_spec(ori_module_spec)
test_module = importlib.util.module_from_spec(test_module_spec)
ori_module_spec.loader.exec_module(ori_module)
test_module_spec.loader.exec_module(test_module)

def test_sort1():
    assert ori_module.sort1([2, 1, 0, -1, -2]) == test_module.sort1([2, 1, 0, -1, -2])
    assert ori_module.sort1([2, 1, 0, -1, -1]) == test_module.sort1([2, 1, 0, -1, -1])
    assert ori_module.sort1([2, 1, 0, 0, -1]) == test_module.sort1([2, 1, 0, 0, -1])
    assert ori_module.sort1([2, 1, 0, 0, 0]) == test_module.sort1([2, 1, 0, 0, 0])
    assert ori_module.sort1([1, 0, 0, -1, -2]) == test_module.sort1([1, 0, 0, -1, -2])
    assert ori_module.sort1([1, 0, 0, -1, -1]) == test_module.sort1([1, 0, 0, -1, -1])
    assert ori_module.sort1([1, 0, 0, 0, -1]) == test_module.sort1([1, 0, 0, 0, -1])
    assert ori_module.sort1([1, 0, 0, 0, 0]) == test_module.sort1([1, 0, 0, 0, 0])
    assert ori_module.sort1([1, 1, 0, -1, -2]) == test_module.sort1([1, 1, 0, -1, -2])
    assert ori_module.sort1([1, 1, 0, -1, -1]) == test_module.sort1([1, 1, 0, -1, -1])
    assert ori_module.sort1([1, 1, 0, 0, -1]) == test_module.sort1([1, 1, 0, 0, -1])
    assert ori_module.sort1([1, 1, 0, 0, 0]) == test_module.sort1([1, 1, 0, 0, 0])
    assert ori_module.sort1([0, 0, 0, -1, -2]) == test_module.sort1([0, 0, 0, -1, -2])
    assert ori_module.sort1([0, 0, 0, -1, -1]) == test_module.sort1([0, 0, 0, -1, -1])
    assert ori_module.sort1([0, 0, 0, 0, -1]) == test_module.sort1([0, 0, 0, 0, -1])
    assert ori_module.sort1([0, 0, 0, 0, 0]) == test_module.sort1([0, 0, 0, 0, 0])
