def sampleStats(count):
        summ = 0.0
        minimum = -1
        maximum = -1
        mode = 0
        median = 0
        L = -1
        R = -1
        acc = 0
        n = sum(count)
        for i in range(len(count)):
            summ += count[i]*i
            #minimum
            if count[i]>0 and minimum==-1:
                minimum = i
            #maximum
            if count[i]>0:
                maximum = i
            #mode
            if count[i] > count[mode]:
                mode = i
            #median
            acc += count[i]
            if acc == n//2 and count[i]>0:
                L = i
            elif acc> n//2:
                if R == -1:
                    R = i
                if L == -1:
                    L = R
        if n%2 == 0:
            median = (L+R)/2.0
        else:
            median = R
        
        return [minimum, maximum, summ/n, median, mode]

